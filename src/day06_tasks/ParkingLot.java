package day06_tasks;

public class ParkingLot {

    public static void main(String[] args) {

        int cars = 10;
        System.out.println("\"There are " + cars + " cars in the parking lot\" " + "\n" + "2 more cars drive in");
        cars = cars+2;
        System.out.println("There are " + cars + " cars in the parking lot" + "\n");
        cars = cars-4;
        System.out.println("There are 8 cars in the parking lot");
        cars = 8;
        cars = cars-3;
        System.out.println("There are " + cars + " left in the parking lot");







    }
}
