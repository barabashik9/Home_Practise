package day06_tasks;

public class KgToPounds {

    public static void main(String[] args) {

        int kg = 10;
        double pounds = kg * 2.20462;


        System.out.println(kg + " kg equal to " + pounds);


        kg = 20;
        pounds = kg * 2.20462;

        System.out.println(kg + " kg equal to " + pounds);




    }
}
