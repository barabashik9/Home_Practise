package day04_tasks;

public class PrintDate {

    public static void main(String[] args) {

        int month = 9;
        int day = 7;
        int year = 2023;

        System.out.println("\n" + month + "/" + day + "/" + year + "\n");
        System.out.println(month + "_" + day + "_" + year+ "\n");
        System.out.println(month + "-" + day + "-" + year+ "\n");
        System.out.println(month + "|" + day + "|" + year+ "\n");
        System.out.println(month + "<>" + day + "<>" + year);

    }
}